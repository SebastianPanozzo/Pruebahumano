     /*
        #### Creación de Pilas

```java
Stack<Integer> pila = new Stack<>();
```

- `Stack<>`: Crea una pila vacía.

#### Añadir Elementos a la Pila

```java
pila.push(10);  // Añade 10 a la cima de la pila
pila.push(20);  // Añade 20 a la cima de la pila
```

- `pila.push(10)`: Añade un elemento a la cima de la pila.

#### Eliminar Elementos de la Pila

```java
pila.pop();  // Elimina el elemento en la cima de la pila
```

- `pila.pop()`: Elimina y devuelve el elemento en la cima de la pila.

#### Mostrar Elementos de la Pila

```java
System.out.println(pila.peek());  // Muestra el elemento en la cima de la pila sin eliminarlo
System.out.println(pila);  // Imprime toda la pila
```

- `pila.peek()`: Muestra el elemento en la cima de la pila sin eliminarlo.
- `System.out.println(pila)`: Imprime toda la pila.

         */
