public class Cola_Explicada {
     /*
        #### Creación de Colas

```java
Queue<Integer> cola = new LinkedList<>();
```

- `Queue<>`: Interfaz para una cola.
- `LinkedList<>`: Implementación de la cola.

#### Añadir Elementos a la Cola

```java
cola.offer(10);  // Añade 10 al final de la cola
cola.offer(20);  // Añade 20 al final de la cola
```

- `cola.offer(10)`: Añade un elemento al final de la cola.

#### Eliminar Elementos de la Cola

```java
cola.poll();  // Elimina el elemento en la cabeza de la cola
```

- `cola.poll()`: Elimina y devuelve el elemento en la cabeza de la cola.

#### Mostrar Elementos de la Cola

```java
System.out.println(cola.peek());  // Muestra el elemento en la cabeza de la cola sin eliminarlo
System.out.println(cola);  // Imprime toda la cola
```

- `cola.peek()`: Muestra el elemento en la cabeza de la cola sin eliminarlo.
- `System.out.println(cola)`: Imprime toda la cola.

         */
}
